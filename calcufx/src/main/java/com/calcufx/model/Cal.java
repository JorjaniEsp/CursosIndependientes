package com.calcufx.model;

public class Cal {
    public double suma(double n1, double n2){
        return n1 + n2;
    }

    public double rest(double n1, double n2){
        return n1 - n2;
    }

    public double mult(double n1, double n2){
        return n1 * n2;
    }

    public double div(double n1, double n2){
        return n1 / n2;
    }
}
