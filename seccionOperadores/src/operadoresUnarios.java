public class operadoresUnarios {
    public static void main(String[] args) {
        int i = -5;
        int j = +i; // j = (1) * i => -5

        int k = -i; // K = (-1) * i => 5

        i = 6;
        j = +i; // no se cambia el positivo, es + * +

        k = -i; // si se cambia a negativo, - * +



    }
}